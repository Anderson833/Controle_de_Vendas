    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
     
              Connection conn=Conexao_BD.getConnection();
         
          if (tbusu.getSelectedRow() != -1) {
        
             try {
           
          
            String sql="update usuario set nome='"+txtnome.getText()+"',endereco='"+txtendereco.getText()+"', telefone='"+txttelefone.getText()+"',login='"+txtUsuario.getText()+"'"
                    + " ,senha='"+txtSenha.getText()+"' where idUsu=?";
            
            
            
            PreparedStatement patm = conn.prepareStatement(sql);
            
            patm.setInt(1,Integer.parseInt(txtIDusu.getText()));
            
             int res= patm.executeUpdate();
            
            if(res>0){
                JOptionPane.showMessageDialog(null,"Usuário Atualizador com Sucesso !","Sucesso!",JOptionPane.INFORMATION_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(null,"Usuário não Atualizador !","Error",JOptionPane.ERROR_MESSAGE);
            }
            
            patm.close();
            conn.close();
            
        } catch (Exception e) {
        }
     
         
        }
        
        
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      
             try {

           

            Connection Conn = Conexao_BD.getConnection();

            String sql="SELECT * FROM usuario";

            PreparedStatement Patm = Conn.prepareStatement(sql);

            ResultSet Rst = Patm.executeQuery();

            DefaultTableModel md = (DefaultTableModel) tbusu.getModel();

            while(Rst.next()){

                String [] lista = new String[]{Rst.getString("idUsu"),Rst.getString("nome"),Rst.getString("endereco")
                    ,Rst.getString("telefone"),Rst.getString("login") ,Rst.getString("senha")};

                md.addRow(lista);

            }
           Rst.close();
            Patm.close();
            Conn.close();

        } catch (SQLException e) {
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

