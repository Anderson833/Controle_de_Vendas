    private void adicionarCliActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adicionarCliActionPerformed
       
          int n=Integer.parseInt(txtIDUsu.getText());
          int tel=Integer.parseInt(txttelefone.getText());
        
        try {
            
             Connection conn = Conexao_BD.getConnection();
            
            Statement stm = conn.createStatement();
            
            String sql="insert into usuario values("+n+",'"+txtnome.getText()+"','"+txtendereco.getText()+"','"
                    +tel+"','"+txtUsuario.getText()+"','"+txtSenha.getText()+"')";
            int res= stm.executeUpdate(sql);
            
            if(res>0){
                JOptionPane.showMessageDialog(null,"Usuário Adicionador com Sucesso !","Sucesso!",JOptionPane.INFORMATION_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(null,"Usuário não Adicionador !","Error",JOptionPane.ERROR_MESSAGE);
            }
            
            stm.close();
            conn.close();
            
            
            
            
            
        } catch (Exception e) {
        }
        
    }//GEN-LAST:event_adicionarCliActionPerformed

