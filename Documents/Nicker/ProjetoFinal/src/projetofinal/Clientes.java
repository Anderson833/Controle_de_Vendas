    private void exibirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_exibirActionPerformed
  
           try {
            Class.forName("com.mysql.jdbc.Driver");
            
            Connection Conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/venda","root","");
            
            String sql="select * from Cliente ";
            
            PreparedStatement Patm = Conn.prepareStatement(sql);
            
            ResultSet Rst = Patm.executeQuery();
            
           
             DefaultTableModel modelo = (DefaultTableModel) tbcli.getModel();
            while(Rst.next()){
                
                String [] linha = new String[]{Rst.getString("Cod_cli"),Rst.getString("Cod_vend"),Rst.getString("nome"),Rst.getString("endereco"),Rst.getString("telefone")};
                  
              modelo.addRow(linha);
                
               
                
                
            } 
               
            
            Rst.close();
            Patm.close();
            Conn.close();
            
            
            
            
        } catch (Exception ex) {
            Logger.getLogger(Clientes.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
        
    }//GEN-LAST:event_exibirActionPerformed

    private void excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirActionPerformed
            try {
             Class.forName("com.mysql.jdbc.Driver");
            
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/venda","root","");
          
            String sql="delete from Cliente where Cod_cli=?";
            
            
            
            PreparedStatement patm = conn.prepareStatement(sql);
            
            patm.setInt(1,Integer.parseInt(txtcli.getText()));
            
             int res= patm.executeUpdate();
            
            if(res>0){
                JOptionPane.showMessageDialog(null,"Cliente excluido com Sucesso !","Sucesso!",JOptionPane.INFORMATION_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(null,"Cliente não excluido !","Error",JOptionPane.ERROR_MESSAGE);
            }
            
            patm.close();
            conn.close();
            
        } catch (Exception e) {
        }
        
    }//GEN-LAST:event_excluirActionPerformed

